            <p>Tanaman tumbuh sentosa di lantai atas dengan sinar matahari melimpah dan sirkulasi udara lancar. Selain <em>D. stratiotes </em>forma alba, ia juga mengoleksi <em>D. antennatum</em> dan <em>D. striaenopsis</em>. Keduanya sama-sama alba. Antenantum alba mempunyai petal, sepal, dan labellum berwarna putih. Tanduknya melintir kuning kehijauan. Penampilan striaenopsis juga tak kalah istimewa. Seluruh bagian bunga dendrobium dari seksi phalaenanthe itu berwarna putih bersih.</p>
